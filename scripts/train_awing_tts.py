def _edge_tts_baseline(gen_text, out_path):
    """Synthesize the same text via Edge TTS (Swahili baseline)."""
    try:
        import asyncio, edge_tts
    except ImportError:
        return False
    async def _gen():
        comm = edge_tts.Communicate(gen_text, "sw-KE-ZuriNeural", rate="-20%")
        await comm.save(str(out_path))
    try:
        asyncio.run(_gen())
        return out_path.exists() and out_path.stat().st_size > 1000
    except Exception:
        return False


def _vits_synthesize(model, tokenizer, text, device, out_path, label="VITS"):
    """Synthesize text through a VITS model and write a WAV.
    Returns (ok, peak_float) where peak_float is the absolute peak of the
    raw model waveform (range [0, ~1]). peak < 0.001 indicates the model
    collapsed to silence."""
    import torch
    try:
        inputs = tokenizer(text, return_tensors="pt")
        inputs = {k: v.to(device) for k, v in inputs.items()}
        with torch.no_grad():
            outputs = model(**inputs)
        waveform = outputs.waveform[0].cpu().numpy()
        peak_float = float(np.max(np.abs(waveform))) if waveform.size else 0.0
        # Normalize to int16 range, but if model output is near-silent,
        # don't artificially amplify — keep it silent so the listener
        # hears the real model state.
        if peak_float > 0.01:
            scale = 0.95 / peak_float * 32767  # peak-normalize to -0.4 dBFS
        else:
            scale = 32767  # leave near-silent output as-is
        waveform_int = (waveform * scale).astype(np.int16)
        audio = AudioSegment(
            waveform_int.tobytes(),
            frame_rate=model.config.sampling_rate,
            sample_width=2,
            channels=1,
        )
        audio.export(str(out_path), format="wav")
        ok = out_path.exists() and out_path.stat().st_size > 1000
        return ok, peak_float
    except Exception as e:
        print(f"    {label} failed for {text!r}: {e}")
        return False, 0.0


