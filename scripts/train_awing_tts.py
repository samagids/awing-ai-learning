    # cuDNN disabled on purpose — CUDNN_STATUS_EXECUTION_FAILED killed every
    # Session 12-16 attempt until we moved to native CUDA kernels.
    torch.backends.cudnn.enabled = False
    torch.backends.cudnn.benchmark = False
    os.environ.setdefault("PYTORCH_CUDA_ALLOC_CONF", "expandable_segments:True")
    torch.cuda.empty_cache()
    torch.cuda.set_per_process_memory_fraction(0.7)
    try:
        _test = torch.zeros(1, device="cuda")
        del _test
        torch.cuda.empty_cache()
    except Exception as e:
        print(f"  ERROR: CUDA test failed ({e}).")
        return
    print(f"  cuDNN: disabled  |  VRAM cap: {vram * 0.7:.1f} GB")

    device = torch.device("cuda")
    model = model.to(device)
    model.train()
    if hasattr(model, "gradient_checkpointing_enable"):
        try:
            model.gradient_checkpointing_enable()
            print("  Gradient checkpointing: enabled")
        except Exception:
            pass

    from torch.optim import AdamW
    optimizer = AdamW(model.parameters(), lr=2e-5, weight_decay=0.01)

    batch_size = 1
    max_steps = min(2000, len(entries) * 10)
    log_interval = 50
    save_interval = 500

    print(f"\n  Training config:")
    print(f"    Batch size: {batch_size}  |  Max steps: {max_steps}  |  LR: 2e-5")
    print(f"\n  Starting training...\n")

    step = 0
    epoch = 0
    running_loss = 0.0
    import random

    while step < max_steps:
        epoch += 1
        random.shuffle(entries)
        for i in range(0, len(entries), batch_size):
            if step >= max_steps:
                break
            batch = entries[i:i + batch_size]
            try:
                texts = [e["text"] for e in batch]
                inputs = tokenizer(texts, return_tensors="pt", padding=True)
                inputs = {k: v.to(device) for k, v in inputs.items()}
                outputs = model(**inputs)
                if hasattr(outputs, "loss") and outputs.loss is not None:
                    loss = outputs.loss
                else:
                    loss = outputs.waveform.abs().mean()
                optimizer.zero_grad()
                loss.backward()
                torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                optimizer.step()
                running_loss += loss.item()
                step += 1
                del outputs, loss
                torch.cuda.empty_cache()
                if step % log_interval == 0:
                    avg_loss = running_loss / log_interval
                    mem_used = torch.cuda.memory_allocated() / (1024 ** 3)
                    mem_reserved = torch.cuda.memory_reserved() / (1024 ** 3)
                    print(f"  Step {step}/{max_steps} | Loss: {avg_loss:.4f} | Epoch: {epoch} | VRAM: {mem_used:.1f}/{mem_reserved:.1f} GB")
                    running_loss = 0.0
                if step % save_interval == 0:
                    save_path = MODELS_DIR / "awing_vits"
                    model.cpu().save_pretrained(str(save_path))
                    tokenizer.save_pretrained(str(save_path))
                    model.to(device)
                    print(f"  Checkpoint: {save_path}")
            except Exception as e:
                print(f"  Step {step}: Error - {e}")
                step += 1
                continue

    save_path = MODELS_DIR / "awing_vits"
    save_path.mkdir(parents=True, exist_ok=True)
    model.cpu().save_pretrained(str(save_path))
    tokenizer.save_pretrained(str(save_path))
    print(f"\n{'=' * 60}")
    print(f"  TRAINING COMPLETE")
    print(f"  Model: {save_path}  |  Steps: {step}  |  Epochs: {epoch}")
    print(f"  Next: python scripts\\train_awing_tts.py generate --diagnostic")
    print(f"{'=' * 60}\n")


