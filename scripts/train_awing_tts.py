def _whisper_transcribe(model, clip_path):
    try:
        # language='sw' (Swahili) gives the closest Bantu phoneme match for ASR
        result = model.transcribe(str(clip_path), fp16=False, language="sw")
        text = result.get("text", "").strip()
        if text and text.lower() not in ("you", "the", "a", "i", ".", ""):
            return text
    except Exception:
        pass
    return None


