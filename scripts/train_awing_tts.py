def parse_srt(srt_path):
    """Parse SRT subtitles, robust to YouTube auto-generated \\r\\r\\n endings."""
    raw = Path(srt_path).read_bytes()
    raw = raw.replace(b"\r\r\n", b"\n").replace(b"\r\n", b"\n").replace(b"\r", b"\n")
    if raw.startswith(b"\xef\xbb\xbf"):
        raw = raw[3:]
    text_content = raw.decode("utf-8", errors="replace")
    lines = text_content.split("\n")

    TIME_RE = re.compile(
        r"(\d{2}):(\d{2}):(\d{2})[,.](\d{3})\s*-->\s*(\d{2}):(\d{2}):(\d{2})[,.](\d{3})"
    )

    entries = []
    i = 0
    while i < len(lines):
        line = lines[i].strip()
        time_match = TIME_RE.match(line)
        if time_match:
            g = time_match.groups()
            start = int(g[0]) * 3600 + int(g[1]) * 60 + int(g[2]) + int(g[3]) / 1000.0
            end = int(g[4]) * 3600 + int(g[5]) * 60 + int(g[6]) + int(g[7]) / 1000.0
            i += 1
            while i < len(lines) and not lines[i].strip():
                i += 1
            text_lines = []
            while i < len(lines):
                tl = lines[i].strip()
                if not tl:
                    i += 1
                    break
                if tl.isdigit() and i + 1 < len(lines) and TIME_RE.match(lines[i + 1].strip()):
                    break
                text_lines.append(tl)
                i += 1
            text = " ".join(text_lines).strip()
            text = re.sub(r"<[^>]+>", "", text)
            text = re.sub(r"\[.*?\]", "", text).strip()
            if text:
                entries.append({"start_sec": start, "end_sec": end, "text": text})
        else:
            i += 1
    return entries


# ===================================================================
# Text normalization — Awing → Makaa tokenizer chars
# ===================================================================
def awing_to_makaa(text):
    """Map Awing orthography to characters the mcp tokenizer understands.

    Makaa (mcp) shares the 5-vowel Bantu system + nasals with Awing but
    lacks Awing's special vowels and tone diacritics. We map:
       ɛ→e  ɔ→o  ə→e  ɨ→i  ŋ→ng
    and strip tone diacritics via NFD decomposition (keep the base letter,
    drop combining marks). This was the same map used for the bss (Akoose)
    base in Sessions 12-16 and worked — both Akoose and Makaa lack these
    Awing-specific chars, so the map transfers.
    """
    import unicodedata
    char_map = {
        'ɛ': 'e', 'ɔ': 'o', 'ə': 'e', 'ɨ': 'i', 'ŋ': 'ng',
        'Ɛ': 'E', 'Ɔ': 'O', 'Ə': 'E', 'Ɨ': 'I', 'Ŋ': 'NG',
    }
    result = []
    for ch in text:
        if ch in char_map:
            result.append(char_map[ch])
        else:
            decomposed = unicodedata.normalize('NFD', ch)
            base = ''.join(c for c in decomposed if unicodedata.category(c) != 'Mn')
            result.append(base if base else ch)
    return ''.join(result)


