/// Awing language alphabet data extracted from AwingOrthography2005.pdf
/// by Alomofor Christian and Stephen C. Anderson.
///
/// The Awing alphabet has 22 consonants and 9 vowels.
/// All Awing words end with a vowel (except "nda'" meaning "only").

class AwingLetter {
  final String letter;
  final String upperCase;
  final String phoneme; // IPA representation
  final String type; // 'consonant' or 'vowel'
  final String exampleWord;
  final String exampleEnglish;
  final String? description; // kid-friendly pronunciation hint

  const AwingLetter({
    required this.letter,
    required this.upperCase,
    required this.phoneme,
    required this.type,
    required this.exampleWord,
    required this.exampleEnglish,
    this.description,
  });
}

